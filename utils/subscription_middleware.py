logger = logging.getLogger(__name__)

def subscription_required(allow_trial: bool = True, send_message: bool = True):
    """
    –î–µ–∫–æ—Ä–∞—Ç–æ—Ä –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏ –ø–æ–¥–ø–∏—Å–∫–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
    
    Args:
        allow_trial: –†–∞–∑—Ä–µ—à–∏—Ç—å –¥–æ—Å—Ç—É–ø –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è–º –Ω–∞ —Ç—Ä–∏–∞–ª–µ
        send_message: –û—Ç–ø—Ä–∞–≤–ª—è—Ç—å —Å–æ–æ–±—â–µ–Ω–∏–µ –æ–± –æ—à–∏–±–∫–µ –¥–æ—Å—Ç—É–ø–∞
    """
    def decorator(func: Callable) -> Callable:
        @wraps(func)
        def wrapper(update: Update, context: CallbackContext, *args, **kwargs) -> Any:
            try:
                user_id = update.effective_user.id
                username = update.effective_user.username
                
                # –û–±–Ω–æ–≤–ª—è–µ–º –∞–∫—Ç–∏–≤–Ω–æ—Å—Ç—å –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
                subscription_service.update_user_activity(user_id)
                
                # –ü—Ä–æ–≤–µ—Ä—è–µ–º –¥–æ—Å—Ç—É–ø
                access_info = subscription_service.check_user_access(user_id)
                
                # –õ–æ–≥–∏—Ä—É–µ–º –ø–æ–ø—ã—Ç–∫—É –¥–æ—Å—Ç—É–ø–∞
                logger.info(f"üîê –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å {user_id} (@{username}) –ø—ã—Ç–∞–µ—Ç—Å—è –ø–æ–ª—É—á–∏—Ç—å –¥–æ—Å—Ç—É–ø: {access_info['status']}")
                
                # –ï—Å–ª–∏ –¥–æ—Å—Ç—É–ø —Ä–∞–∑—Ä–µ—à–µ–Ω
                if access_info['has_access']:
                    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –º–æ–∂–Ω–æ –ª–∏ —Ç—Ä–∏–∞–ª—å–Ω—ã–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è–º –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å —ç—Ç—É —Ñ—É–Ω–∫—Ü–∏—é
                    if not allow_trial and access_info['is_trial']:
                        if send_message:
                            message = "üîí –≠—Ç–∞ —Ñ—É–Ω–∫—Ü–∏—è –¥–æ—Å—Ç—É–ø–Ω–∞ —Ç–æ–ª—å–∫–æ –¥–ª—è –ø–æ–ª–Ω—ã—Ö –ø–æ–¥–ø–∏—Å–æ–∫.\n–û—Ñ–æ—Ä–º–∏—Ç–µ –ø–ª–∞—Ç–Ω—É—é –ø–æ–¥–ø–∏—Å–∫—É –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è –¥–æ—Å—Ç—É–ø–∞."
                            if update.callback_query:
                                update.callback_query.answer(message, show_alert=True)
                            else:
                                update.message.reply_text(message)
                        return None
                    
                    # –í—ã–ø–æ–ª–Ω—è–µ–º –æ—Å–Ω–æ–≤–Ω—É—é —Ñ—É–Ω–∫—Ü–∏—é
                    return func(update, context, *args, **kwargs)
                
                # –î–æ—Å—Ç—É–ø –∑–∞–ø—Ä–µ—â–µ–Ω
                if access_info['status'] == 'not_registered':
                    # –ü—Ä–µ–¥–ª–∞–≥–∞–µ–º —Ç—Ä–∏–∞–ª
                    if send_message:
                        message = (
                            "üö´ –£ –≤–∞—Å –Ω–µ—Ç –¥–æ—Å—Ç—É–ø–∞ –∫ —Å–∏—Å—Ç–µ–º–µ.\n\n"
                            "üí° –•–æ—Ç–∏—Ç–µ –ø–æ–ª—É—á–∏—Ç—å –±–µ—Å–ø–ª–∞—Ç–Ω—ã–π —Ç—Ä–∏–∞–ª –Ω–∞ 1 –¥–µ–Ω—å?\n"
                            "–û–±—Ä–∞—Ç–∏—Ç–µ—Å—å –∫ –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä—É: @admin"
                        )
                        if update.callback_query:
                            update.callback_query.answer(message, show_alert=True)
                        else:
                            update.message.reply_text(message)
                elif access_info['status'] == 'expired':
                    if send_message:
                        message = (
                            "‚è∞ –í–∞—à–∞ –ø–æ–¥–ø–∏—Å–∫–∞ –∏—Å—Ç–µ–∫–ª–∞.\n\n"
                            "üí≥ –ü—Ä–æ–¥–ª–∏—Ç–µ –ø–æ–¥–ø–∏—Å–∫—É –¥–ª—è –ø—Ä–æ–¥–æ–ª–∂–µ–Ω–∏—è —Ä–∞–±–æ—Ç—ã.\n"
                            "–û–±—Ä–∞—Ç–∏—Ç–µ—Å—å –∫ –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä—É: @admin"
                        )
                        if update.callback_query:
                            update.callback_query.answer(message, show_alert=True)
                        else:
                            update.message.reply_text(message)
                elif access_info['status'] == 'blocked':
                    if send_message:
                        message = (
                            "üö´ –í–∞—à –∞–∫–∫–∞—É–Ω—Ç –∑–∞–±–ª–æ–∫–∏—Ä–æ–≤–∞–Ω.\n\n"
                            "üìû –î–ª—è —Ä–∞–∑–±–ª–æ–∫–∏—Ä–æ–≤–∫–∏ –æ–±—Ä–∞—Ç–∏—Ç–µ—Å—å –∫ –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä—É: @admin"
                        )
                        if update.callback_query:
                            update.callback_query.answer(message, show_alert=True)
                        else:
                            update.message.reply_text(message)
                else:
                    if send_message:
                        message = access_info.get('message', '‚ùå –ù–µ—Ç –¥–æ—Å—Ç—É–ø–∞ –∫ —Å–∏—Å—Ç–µ–º–µ')
                        if update.callback_query:
                            update.callback_query.answer(message, show_alert=True)
                        else:
                            update.message.reply_text(message)
                
                return None
                
            except Exception as e:
                logger.error(f"–û—à–∏–±–∫–∞ –ø—Ä–æ–≤–µ—Ä–∫–∏ –ø–æ–¥–ø–∏—Å–∫–∏ –≤ {func.__name__}: {e}")
                if send_message:
                    message = "‚ùå –û—à–∏–±–∫–∞ –ø—Ä–æ–≤–µ—Ä–∫–∏ –¥–æ—Å—Ç—É–ø–∞. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –ø–æ–∑–∂–µ."
                    if update.callback_query:
                        update.callback_query.answer(message, show_alert=True)
                    else:
                        update.message.reply_text(message)
                return None
        
        return wrapper
    return decorator

def trial_allowed(func: Callable) -> Callable:
    """–°–æ–∫—Ä–∞—â–µ–Ω–Ω—ã–π –¥–µ–∫–æ—Ä–∞—Ç–æ—Ä –¥–ª—è —Ñ—É–Ω–∫—Ü–∏–π, –¥–æ—Å—Ç—É–ø–Ω—ã—Ö —Ç—Ä–∏–∞–ª—å–Ω—ã–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è–º"""
    return subscription_required(allow_trial=True, send_message=True)(func)

